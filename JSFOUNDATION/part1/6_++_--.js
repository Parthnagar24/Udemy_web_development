let count = 5;

// Postfix increment
let result1 = count++; // result1 is 5, count is now 6
console.log("result1:", result1, "count:", count);

// Prefix increment
let result2 = ++count; // count is now 7, result2 is 7
console.log("result2:", result2, "count:", count);

let num = 10;

// Postfix decrement
let result3 = num--; // result3 is 10, num is now 9
console.log("result3:", result3, "num:", num);

// Prefix decrement
let result4 = --num; // num is now 8, result4 is 8
console.log("result4:", result4, "num:", num);
