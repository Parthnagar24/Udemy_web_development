// Compound assignment operators (shorthand)
score += 20; // Same as: score = score + 20;
score -= 10; // Same as: score = score - 10;
score *= 2; // Same as: score = score * 2;
score /= 5; // Same as: score = score / 5;
score %= 3; // Same as: score = score % 3;
