const fruits = ["apple", "banana", "cherry", "date"];
const appleIndex = fruits.lastIndexOfindexOf("apple");
console.log(appleIndex); // Output: 0

// The lastIndexOf method returns the last index at which a given element can be found in the array, or -1 if it is not present.
// It searches the array from the end to the beginning, which is useful when you want to find the last occurrence of an element.
// In this case, since "apple" is the first element, the output is 0
