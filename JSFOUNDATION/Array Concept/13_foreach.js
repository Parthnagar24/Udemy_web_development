const numbers = [1, 2, 3, 4, 5, 6];
numbers.foreach((num) => console.log(num * 3));
// Output: 3, 6, 9, 12, 15, 18
// The forEach method executes a provided function once for each array element. In this case,
// it multiplies each number by 3 and logs the result to the console.
// The forEach method does not return a new array; it simply performs the operation on each element
