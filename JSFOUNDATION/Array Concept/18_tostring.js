const numbers = [1, 2, 3, 4, 5, 6];
numbers.toString();
console.log(numbers.toString()); // Output: "1,2,3,4,5,6"
// The toString method converts an array to a string by joining its elements with commas.
// This method does not modify the original array; it simply returns a string representation of the array.
// The output is a single string where each element of the array is separated by a comma.
