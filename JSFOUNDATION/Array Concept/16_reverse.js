const numbers = [1, 2, 3, 4, 5, 6];
numbers.reverse();
console.log(numbers); // Output: [6, 5, 4, 3, 2, 1]
// The reverse method reverses the elements of an array in place, meaning it modifies the original
// array. The first element becomes the last, and the last becomes the first.
// This method does not return a new array; it simply changes the order of the elements in the existing array.
// After calling reverse, the original array is now in reverse order.
